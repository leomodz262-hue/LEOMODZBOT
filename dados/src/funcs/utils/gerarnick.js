// Gerador de Estilos Local
class StyleGenerator {
  static normalizeText(text) {
    return text.normalize('NFKD').replace(/[\u0300-\u036f]/g, '');
  }

  static applyStyle(text, styleMap) {
    const normalized = this.normalizeText(text);
    let styled = '';

    for (const char of normalized) {
      // Usa o caractere diretamente como chave no styleMap
      styled += styleMap[char] || char; // MantÃ©m o caractere original se nÃ£o houver mapeamento
    }

    return styled;
  }

  static generateStyles(text) {
    if (!text || typeof text !== 'string' || text.length === 0) {
      return [];
    }

    const styles = [];

    // Gera todos os estilos disponÃ­veis
    for (const [styleName, styleMap] of Object.entries(FONT_STYLES)) {
      if (styleName !== 'normal') {
        styles.push(this.applyStyle(text, styleMap));
      }
    }

    // Adiciona alguns estilos compostos
    styles.push(text.split('').join('á †')); // Texto separado
    styles.push(text.split('').join(' ')); // EspaÃ§ado
    styles.push(text.toUpperCase()); // MaiÃºsculas
    styles.push(text.toLowerCase()); // MinÃºsculas

    // Remove estilos vazios ou duplicados
    return styles
      .filter(style => style && style.trim())
      .filter((style, index, self) => self.indexOf(style) === index);
  }
}

/**
 * Gera estilos de texto diferentes (100% local)
 * @param {string} text - Texto para estilizar
 * @returns {Array<string>} Array de estilos
 */
function styleText(text) {
  try {
    // ValidaÃ§Ãµes
    if (!text || typeof text !== 'string') {
      throw new Error('Texto invÃ¡lido');
    }

    if (text.length > 100) {
      throw new Error('Texto muito longo (mÃ¡ximo 100 caracteres)');
    }

    // Gera estilos locais
    const styles = StyleGenerator.generateStyles(text);

    return styles;
  } catch (error) {
    console.error('Erro ao gerar estilos:', error);
    // Retorna array vazio em caso de erro
    return [];
  }
}

